package Classe;

/**
 *
 * @author Pichau
 */
public abstract class Pessoa {
    
    protected String nome;
    protected String cpf;
    protected String rg;
    
    // Getters & Setters
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }
    
    
    
    
    
}
